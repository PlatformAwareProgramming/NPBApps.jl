      function nodedim(num)

#---------------------------------------------------------------------
#---------------------------------------------------------------------

#---------------------------------------------------------------------
#
#  compute the exponent where num = 2**nodedim
#  NOTE: assumes a power-of-two number of nodes
#
#---------------------------------------------------------------------

#      implicit none

#---------------------------------------------------------------------
#  input parameters
#---------------------------------------------------------------------
#      integer num

#---------------------------------------------------------------------
#  local variables
#---------------------------------------------------------------------
#      DOUBLEPRECISION fnum


      fnum = float(num)
      nodedim = log(fnum)/log(2.0e+0) + 0.00001

      return nothing
      end


